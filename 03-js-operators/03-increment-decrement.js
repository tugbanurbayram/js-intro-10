// Increment: used to increase the number by one
let num1 = 5;

console.log(num1++); // 5
console.log(num1); // 6



// Decrement: used to decrease the number by one

let n1 = 10;

let n2 = --n1 * 5;

console.log(n2); // 45


let number = 25;

number++; // 25 but increase next time

console.log(number-- * 10); // 260 